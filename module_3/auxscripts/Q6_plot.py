plt.plot(x, data8, label="L: 9; Init: -1")
plt.plot(x, data9, label="L: 9; Init: random")
plt.plot(x, data13, label="L: 10; Init: +1")
plt.plot(x, data14, label="L: 10; Init: -1")
plt.plot(x, data15, label="L: 10; Init: random")
plt.xlabel("Iterations")
plt.ylabel("Energy per spin")
plt.title("Energy per Spin : k$_B$T = 3.9")
ax = plt.gca()
ax.set_ylim([-3, 0])
plt.legend()
plt.savefig("figures/Q6_Energy.png")

