data12 = np.loadtxt("data/Q6.2_M_T-3.9_init_rand.dat")
data13 = np.loadtxt("data/Q6.3_E_T-3.9_init_1.dat")
data14 = np.loadtxt("data/Q6.3_E_T-3.9_init_m1.dat")
data15 = np.loadtxt("data/Q6.3_E_T-3.9_init_rand.dat")
data16 = np.loadtxt("data/Q6.3_M_T-3.9_init_1.dat")
data17 = np.loadtxt("data/Q6.3_M_T-3.9_init_m1.dat")
data18 = np.loadtxt("data/Q6.3_M_T-3.9_init_rand.dat")
x = np.linspace(0,len(data1), len(data1)).astype(int)

plt.plot(x, data1, label="L: 8; Init: +1")
plt.plot(x, data2, label="L: 8; Init: -1")
