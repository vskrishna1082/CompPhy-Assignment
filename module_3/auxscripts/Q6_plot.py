data5 = np.loadtxt("data/Q6.1_M_T-3.9_init_m1.dat")
data6 = np.loadtxt("data/Q6.1_M_T-3.9_init_rand.dat")
data7 = np.loadtxt("data/Q6.2_E_T-3.9_init_1.dat")
